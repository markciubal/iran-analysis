def _point_in_polygon(lon, lat, polygon=IRGC_POLYGON):
    """Ray-casting point-in-polygon test."""
    inside = False
    n = len(polygon)
    j = n - 1
    for i in range(n):
        xi, yi = polygon[i]
        xj, yj = polygon[j]
        if ((yi > lat) != (yj > lat)) and (lon < (xj - xi) * (lat - yi) / (yj - yi) + xi):
            inside = not inside
        j = i
    return inside


def _sample_in_polygon(rng, polygon=IRGC_POLYGON, bbox=IRGC_POLY_BBOX):
    """Rejection-sample a uniform random point inside the polygon."""
    lon_min, lat_min, lon_max, lat_max = bbox
    for _ in range(10_000):
        lon = rng.uniform(lon_min, lon_max)
        lat = rng.uniform(lat_min, lat_max)
        if _point_in_polygon(lon, lat, polygon):
            return lon, lat
    raise RuntimeError("Could not sample point inside IRGC polygon")
